    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
                                              
    try {
        int id = Integer.parseInt(txtId.getText().trim());
        
        // Confirmar antes de eliminar
        int respuesta = JOptionPane.showConfirmDialog(this, 
            "¿Está seguro de eliminar al estudiante con ID: " + id + "?", 
            "Confirmar eliminación", 
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE);
        
        if (respuesta == JOptionPane.YES_OPTION) {
            EstudianteControlador controlador = new EstudianteControlador();
            boolean resultado = controlador.eliminar(id);
            
            if (resultado) {
                JOptionPane.showMessageDialog(this, 
                    "Estudiante eliminado exitosamente", 
                    "Éxito", 
                    JOptionPane.INFORMATION_MESSAGE);
                
                // ¡IMPORTANTE! Limpiar campos Y actualizar tabla
                limpiarCampos();
                cargarDatos();  // ← ESTA LÍNEA ES CLAVE
            } else {
                JOptionPane.showMessageDialog(this, 
                    "Error al eliminar. Verifique que el ID exista", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            }
        }
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, 
            "Error: El ID debe ser un número válido", 
            "Error de formato", 
            JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    
