    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed

    try {
        // 1. Obtener datos de los campos
        int id = Integer.parseInt(txtId.getText().trim());
        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();
        String email = txtEmail.getText().trim();
        int edad = Integer.parseInt(txtEdad.getText().trim());
        String carrera = txtCarrera.getText().trim();
        
        // 2. Crear objeto Estudiante
        Estudiante e = new Estudiante(id, nombre, apellido, email, edad, carrera);
        
        // 3. Llamar al controlador
        EstudianteControlador controlador = new EstudianteControlador();
        boolean resultado = controlador.crear(e); // ← ahora retorna boolean
        
        // 4. Mostrar resultado
        if (resultado) {
            JOptionPane.showMessageDialog(this, 
                """
                Estudiante creado exitosamente
                ID: """ + id + "\n" +
                "Nombre: " + nombre + " " + apellido,
                "Éxito",
                JOptionPane.INFORMATION_MESSAGE);
            
            // 5. Actualizar tabla
            cargarDatos();
            
            // 6. Limpiar campos (opcional)
            // limpiarCampos();
            
        } else {
            JOptionPane.showMessageDialog(this, """
                                                No se pudo crear el estudiante
                                                Posible causa: ID duplicado""",
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
            """
            Error: ID y Edad deben ser numeros
            ID: """ + txtId.getText() + "\n" +
            "Edad: " + txtEdad.getText(),
            "Error de formato",
            JOptionPane.ERROR_MESSAGE);
    } catch (HeadlessException e) {
        JOptionPane.showMessageDialog(this,
            "Error: " + e.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE);
    }
    
    }//GEN-LAST:event_btnCrearActionPerformed

    
    
