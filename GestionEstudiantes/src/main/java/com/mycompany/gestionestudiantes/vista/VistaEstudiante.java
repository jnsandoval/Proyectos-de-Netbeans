private void configurarTabla() {
    tblEstudiantes.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting() && tblEstudiantes.getSelectedRow() >= 0) {
            int fila = tblEstudiantes.getSelectedRow();
            
            txtId.setText(tblEstudiantes.getValueAt(fila, 0).toString());
            txtNombre.setText(tblEstudiantes.getValueAt(fila, 1).toString());
            txtApellido.setText(tblEstudiantes.getValueAt(fila, 2).toString());
            txtEmail.setText(tblEstudiantes.getValueAt(fila, 3).toString());
            txtEdad.setText(tblEstudiantes.getValueAt(fila, 4).toString());
            txtCarrera.setText(tblEstudiantes.getValueAt(fila, 5).toString());
        }
    });
}



private boolean validarDatos() {
    // Validar que ID sea número
    try {
        Integer.parseInt(txtId.getText().trim());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID debe ser número", "Error", JOptionPane.ERROR_MESSAGE);
        txtId.requestFocus();
        return false;
    }
    
    // Validar que nombre no esté vacío
    if (txtNombre.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nombre es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
        txtNombre.requestFocus();
        return false;
    }
    
    return true;
}
    
    
    
