    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

    try {
        // Capturar datos (Cuidado con txtId y txtID, usa solo txtId)
        int id = Integer.parseInt(txtId.getText()); 
        String nom = txtNombre.getText();
        String fec = txtFecha.getText();
        String lug = txtLugar.getText();
        String des = txtDescripcion.getText();

        // Crear modelo y guardar usando el controlador que declaraste arriba
        Evento ev = new Evento(id, nom, fec, lug, des);
        control.agregarEvento(ev); 

        // Mostrar en el Ã¡rea de texto
        txtAreaMostrar.append("ID: " + ev.getID() + ", " + "Nombre: " + ev.getNombre() + ", " + "Fecha: "+ ev.getFecha() + ", " + "Lugar: " + ev.getLugar() + ", " + "Descripcion: " + ev.getDescripcion() + " .");

       
    } catch (NumberFormatException e) {
      
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

