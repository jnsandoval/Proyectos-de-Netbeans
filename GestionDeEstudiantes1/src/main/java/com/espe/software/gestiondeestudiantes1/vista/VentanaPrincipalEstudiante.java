    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // Importamos las clases necesarias (aseg√∫rate de que EstudianteDAO y Estudiante est√©n importadas)
    // El objeto para la conexi√≥n lo declaramos arriba, dentro de la clase VentanaPrincipalEstudiante
        // TODO add your handling code here:
    try {
        // 1. OBTENER DATOS de los 5 campos
        String id = txtID.getText().trim();
        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();
        String email = txtEmail.getText().trim();
        String carrera = txtCarrera.getText().trim();
        
        // 2. VALIDACIONES B√ÅSICAS
        if (id.isEmpty() || nombre.isEmpty() || apellido.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "ID, Nombre y Apellido no pueden estar vac√≠os.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. CREAR EL OBJETO MODELO Estudiante
        // (Asumimos que la Edad es 0 ya que no tienes campo de Edad)
        com.espe.software.gestiondeestudiantes1.Modelo.Estudiante nuevoEstudiante = new com.espe.software.gestiondeestudiantes1.Modelo.Estudiante(
            id,
            nombre,
            apellido,
            email,
            0, // Edad (Poner 0 ya que no hay campo en la GUI)
            carrera
        );
        
        // 4. LLAMAR AL DAO PARA INSERTAR EN MONGODB
        // üö® Aqu√≠ debes asegurar que tienes el objeto estudianteDAO inicializado en la clase.
        // Si no lo tienes, debes agregarlo: private final EstudianteDAO estudianteDAO = new EstudianteDAO();
        
        boolean exito = estudianteDAO.insertarEstudiante(nuevoEstudiante);
        
        if (exito) {
            javax.swing.JOptionPane.showMessageDialog(this, "‚úÖ Estudiante Creado Exitosamente!");
            // Limpiar campos despu√©s de guardar
            txtID.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            txtEmail.setText("");
            txtCarrera.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Error al crear. El ID puede estar duplicado o la conexi√≥n fall√≥.", "Error de Base de Datos", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    } catch (HeadlessException e) {
         javax.swing.JOptionPane.showMessageDialog(this, "Ocurri√≥ un error inesperado: " + e.getMessage(), "Error Grave", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnCrearActionPerformed

